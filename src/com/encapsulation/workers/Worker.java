package com.encapsulation.workers;

public class Worker {
	private int workerEmpSsn;
	private int workerEmpYearOfBirth;
	private String workerEmpFirstName;
	private String workerEmpLastName;
	
	public int getWorkerEmpSsn() {
		return workerEmpSsn;
	}
	public void setWorkerEmpSsn(int workerEmpSsn) {
		this.workerEmpSsn = workerEmpSsn;
	}
	public int getWorkerEmpYearOfBirth() {
		return workerEmpYearOfBirth;
	}
	public void setWorkerEmpYearOfBirth(int workerEmpYearOfBirth) {
		this.workerEmpYearOfBirth = workerEmpYearOfBirth;
	}
	public String getWorkerEmpFirstName() {
		return workerEmpFirstName;
	}
	public void setWorkerEmpFirstName(String workerEmpFirstName) {
		this.workerEmpFirstName = workerEmpFirstName;
	}
	public String getWorkerEmpLastName() {
		return workerEmpLastName;
	}
	public void setWorkerEmpLastName(String workerEmpLastName) {
		this.workerEmpLastName = workerEmpLastName;
	}
	
	

}
